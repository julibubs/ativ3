  <h1>Bolinho de Chuva</h1>
  <p>Receita clássica top </p>

  <h2>Rendimento</h2>
  <p>Aproximadamente 20 bolinhos!</p>

  <h2>Ingredientes</h2>
  <ul>
    <li>2 xícaras (chá) de farinha de trigo</li>
    <li>1/2 xícara (chá) de açúcar</li>
    <li>1 pitada de sal</li>
    <li>2 ovos</li>
    <li>3/4 de xícara (chá) de leite</li>
    <li>1 colher (sopa) de fermento químico (pó)</li>
    <li>1 colher (chá) de essência de baunilha (opcional)</li>
    <li>Óleo para fritar</li>
    <li>Açúcar e canela para polvilhar</li>
  </ul>

  <h2>Modo de Preparo</h2>
  <ol>
    <li>Em uma tigela, misture a farinha, o açúcar e o sal.</li>
    <li>Acrescente os ovos e o leite aos poucos, mexendo até formar uma massa cremosa.</li>
    <li>Adicione o fermento e misture delicadamente.</li>
    <li>Aqueça o óleo em fogo médio (sem estar muito quente).</li>
    <li>Com uma colher, pegue porções da massa e coloque para fritar até dourar.</li>
    <li>Retire, escorra em papel-toalha e passe no açúcar com canela.</li>
    <li>Sirva quentinho.</li>
  </ol>

  <h3>Dicas</h3>
  <ul>
    <li>Se o óleo estiver muito quente, os bolinhos queimam por fora e ficam crus por dentro.</li>
    <li>Se a massa ficar muito dura, coloque um pouquinho mais de leite; se ficar muito líquida, adicione um pouco de farinha.</li>
  </ul>

